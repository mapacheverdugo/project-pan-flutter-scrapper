  final String symbolNative;
  final int decimalDigits;
  final String majorSingle;
  final String majorPlural;
  final String minorSingle;
  final String minorPlural;
  final int isoNum;
  final String isoLetters;
  final int isoDigits;
}

extension CurrencyFormatterExt on Currency {
  String format(double amount) {
    return CurrencyFormatter.format(
      amount / pow(10, decimalDigits),
      _settings,
      decimal: decimalDigits,
    );
  }

  CurrencyFormat get _settings => CurrencyFormat(
    symbol: symbolNative,
    decimalSeparator: '.',
    thousandSeparator: ',',
  );
}
