  const Currency({
    required this.symbol,
    required this.symbolNative,
    required this.decimalDigits,
    required this.majorSingle,
    required this.majorPlural,
    required this.minorSingle,
    required this.minorPlural,
    required this.isoNum,
    required this.isoLetters,
    required this.isoDigits,
  });

  factory Currency.fromIsoLetters(
    String isoLetters, {
    bool returnUnknown = true,
  }) {
    try {
      return Currency.values.firstWhere(
        (currency) => currency.isoLetters == isoLetters,
      );
    } catch (e) {
      if (returnUnknown) {
        return Currency.unknown;
      }
      rethrow;
    }
  }

  factory Currency.fromIsoNum(String isoNum, {bool returnUnknown = true}) {
    try {
      return Currency.values.firstWhere(
        (currency) => currency.isoNum.toString() == isoNum,
      );
    } catch (e) {
      if (returnUnknown) {
        return Currency.unknown;
      }
      rethrow;
    }
  }

  static Currency? tryFromIsoLetters(String? isoLetters) {
    try {
      if (isoLetters == null) return null;
      return Currency.fromIsoLetters(isoLetters, returnUnknown: false);
    } catch (e) {
      return null;
    }
  }

  static Currency? tryFromIsoNum(String isoNum) {
    try {
      return Currency.fromIsoNum(isoNum, returnUnknown: false);
    } catch (e) {
      return null;
    }
  }

